import UIKit

public extension UIColor {
    
    struct WebmotorsColor {
        
        // MARK: - White
        
        public static var grayLightest: UIColor {
            return UIColor(named: "grayLightest") ?? UIColor.white
        }
        
        public static var wineMedium: UIColor {
            return UIColor(named: "wineMedium") ?? UIColor.white
        }
   
    }
}

